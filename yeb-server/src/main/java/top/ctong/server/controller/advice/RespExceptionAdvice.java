@ControllerAdvice
public class RespExceptionAdvice {

    @ResponseBody
    @ExceptionHandler(BaseRespException.class)
    public R<?> defaultExceptionHandler(BaseRespException e) {
        if (e.getHrs() == null) {
            return R.fail(500, e.getMessage());
        }
        return R.fail(e.getHrs());
    }

}
